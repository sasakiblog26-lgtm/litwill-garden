export function buildBasicReading(d: OccultData): BasicReading {
  const sun = d.西洋占星術.太陽.name;
  const moon = d.西洋占星術.月.name;
  const elem = d.四柱推命.日干五行;
  const strongest = d.四柱推命.最強五行;
  const nak = d.インド占星術.ナクシャトラ;
  const dashaLord = d.インド占星術.現在のダシャー.支配星;

  const sunInfo = SUN[sun] ?? { word: "個性", body: "" };
  const elemInfo = ELEMENT[elem] ?? { archetype: "唯一の存在", body: "" };

  const headline = `${sunInfo.word}を宿す、${elemInfo.archetype}`;

  const [y, m, day] = d.meta.birth_date.split("-").map((n) => parseInt(n, 10));
  const birthLabel = `${y}年${m}月${day}日生まれ`;

  const summary =
    `あなたは太陽星座が${sun}、月星座が${moon}。四柱推命では「${elem}」の質を芯に持つ人です。` +
    `西洋・インド・四柱推命の3つの体系が、同じあなたを別々の角度から照らし出しています。`;

  const sections: ReadingSection[] = [
    { label: "あなたの核（太陽星座・" + sun + "）", body: sunInfo.body },
    { label: "心の世界（月星座・" + moon + "）", body: MOON[moon] ?? "" },
    {
      label: "持って生まれた力（四柱推命・日干「" + d.四柱推命.日干 + "／" + elem + "」）",
      body: elemInfo.body + `あなたの中でいちばん強い五行は「${strongest}」。${ELEM_BALANCE_NOTE[strongest] ?? ""}`,
    },
    {
      label: "魂の個性（インド占星術・" + nak.name + "）",
      body: `生まれた瞬間に月が宿っていたナクシャトラは「${nak.name}」（第${nak.pada}パダ）。あなたの魂が今生で帯びる、独自の色あいを示します。`,
    },
    {
      label: "今めぐっている運気（ダシャー・" + dashaLord + "期）",
      body: DASHA[dashaLord] ?? "人生の大きなテーマがめぐっている時期です。",
    },
  ];

  const lockedTeaser = [
    `${sun}×${moon}×${elem} の3体系を統合した「あなた専用の読み解き」`,
    "恋愛・仕事・お金など、悩み別の具体的なアドバイス",
    "今の運気を活かす、明日からの開運アクション",
    "自分のAIに貼れば一生使える「あなた専用の占いプロンプト」",
  ];

  return {
    headline,
    birthLabel,
    summary,
    sections,
    dashaPeriod: d.インド占星術.現在のダシャー.期間,
    timeKnown: d.meta.timeKnown,
    lockedTeaser,
  };
}
