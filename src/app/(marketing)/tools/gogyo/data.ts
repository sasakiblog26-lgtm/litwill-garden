export type Element = "木" | "火" | "土" | "金" | "水";

export function yearToElement(year: number): Element {
  const d = year % 10;
  if (d === 4 || d === 5) return "木";
  if (d === 6 || d === 7) return "火";
  if (d === 8 || d === 9) return "土";
  if (d === 0 || d === 1) return "金";
  return "水"; // 2,3
}

