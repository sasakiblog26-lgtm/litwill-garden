const DEFAULT = { name: "東京（既定）", lat: 35.6895, lon: 139.6917, tzHours: 9 };

export function geocodeJP(place) {
  if (!place || typeof place !== "string") return { ...DEFAULT };
  const p = place.trim();
  for (const [name, lat, lon] of PREF) {
    if (p.includes(name)) return { name, lat, lon, tzHours: 9 };
  }
  return { ...DEFAULT, matched: false };
}
